async function loadWeb3() {
    if (window.ethereum) {
        window.web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: 'eth_requestAccounts' });
    } else {
        alert("Please install MetaMask to use this site.");
    }
}

async function loadBlockchainData() {
    const web3 = window.web3;
    const accounts = await web3.eth.getAccounts();
    window.account = accounts[0];

    window.contract = new web3.eth.Contract(abi, contractAddress);
}

async function mintNFT(occupant, epitaph, metadata) {
    try {
        const MINT_COST = window.web3.utils.toWei("0.01", "ether"); // Set the minting cost in ether (replace 0.01 with the actual cost)

        await window.contract.methods.mint(occupant, epitaph, metadata)
            .send({ from: window.account, value: MINT_COST });

        setStatus("Successfully minted NFT!");
    } catch (error) {
        console.error(error);
        setStatus("Error minting NFT.");
    }
}

function setStatus(message) {
    const status = document.getElementById("status");
    status.innerHTML = message;
}

mintNFTButton.addEventListener("click", async () => {
    const occupant = document.getElementById("occupant").value;
    const epitaph = document.getElementById("epitaph").value;
    const metadata = document.getElementById("metadata").value;

    if (occupant && epitaph && metadata) {
        setStatus("Minting NFT...");
        await mintNFT(occupant, epitaph, metadata);
    } else {
        setStatus("Please enter<br> the occupant,<br> epitaph, and metadata.");
    }
});

async function initApp() {
    await loadWeb3();
    await loadBlockchainData();
}

initApp();